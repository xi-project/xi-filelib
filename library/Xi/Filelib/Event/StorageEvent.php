namespace Xi\Filelib\Event;

use Symfony\Component\EventDispatcher\Event;

/**
 * Plugin event
 */
class StorageEvent extends Event
{
    /**
     * @var string
     */
    private $path;

    /**
     * @param string $path
     */
    public function __construct($path)
    {
        $this->path = $path;
    }

    /**
     * @return string
     */
    public function getPath()
    {
        return $this->path;
    }

    /**
     * @param $path
     * @return $this
     */
    public function setPath($path)
    {
        $this->path = $path;
        return $this;
    }
}
